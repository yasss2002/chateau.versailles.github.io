<div id="un">
		<h4>Sous Louis XIV</h4><br><br>

			<img src="louis14.jpg"/>
			<p>Pour favoriser la construction de la ville, le roi Louis XIV prit deux importantes décisions, le 22 mai 1671, en faisant don de terrains à bâtir contre l'engagement de construire et le paiement d'un droit, modeste, de cinq sols par arpent et le 24 novembre 1672 en rendant insaisissables les immeubles construits.<br><br>

En 1673, est décidée la destruction du vieux village de Versailles. Une nouvelle église Saint-Julien, destinée à remplacer celle de l'ancien village, est édifiée en 1681-1682 près du nouveau cimetière de la Ville Neuve. Dès 1684, commencent les travaux de construction de la nouvelle église Notre-Dame destinée à la remplacer. Située dans l'axe de la rue DauphineNote 1, elle est consacrée en 1686 et devient la paroisse royale de Versailles.<br><br>

En 1682, sont achevées la Petite Écurie et la Grande Écurie destinées à abriter les chevaux de selle et les carrosses royaux. Construites par Jules-Hardouin Mansart, de part et d'autre de l'avenue de Paris, elles complètent la place d'Armes face au château.<br>

</p><br><br><br>

</div>

<div id="deux">

		<h4>Sous Louis XV</h4><br><br>

		<img src="louis15.jpg"/>
		<p>À la mort de Louis XIV, le 1er septembre 1715, le régent Philippe d'Orléans, décide de transférer la Cour à Paris. Commence alors une phase de déclin pour la ville qui voit sa population diminuer rapidement de moitié : le marché immobilier s'effondre.<br><br>

La situation se rétablit sept ans plus tard, le 15 juin 1722, avec le retour du roi Louis XV, alors âgé de douze ans.<br><br>

En 1737, l'étang de Clagny, situé au nord de la Ville Neuve et qui était alors devenu un cloaque recevant tous les égouts, fut comblé et permit de récupérer vingt-quatre hectares immédiatement ouverts à la construction.<br>

En 1740, une émeute se produisit dans le magasin des farines du marché de la Ville Neuve, appelé le « Poids le RoiNote 2 ». Dans un contexte de mauvaise récolte, des Versaillaises, voulant s'opposer à l'enlèvement de farines par les boulangers parisiens, furent réprimées par les gardes suisses.<br>

</p><br><br>

