		<h4>Sous Louis XIII</h4><br><br>
			<p>En 1623, le roi Louis XIII fait construire un rendez-vous de chasse sur un terrain de cent dix-sept arpents (soit environ 350 hectares) acheté à divers propriétaires.<br><br>

Le 8 avril 1632, Louis XIII achète la totalité de la seigneurie de Versailles à son dernier seigneur, Jean-François de Gondi, archevêque de Paris pour la somme de 66 000 livres. C'est le tournant décisif dans l'installation de la royauté à Versailles. Cette même année, il nomme son valet de chambre, Arnault, comme gouverneur de Versailles, dont la fonction était d'administrer le domaine, c'est-à-dire tant la ville que le château.<br><br>

En 1634, sont achevés les travaux confiés à l'architecte Philibert Le Roy. Le premier manoir est reconstruit et agrandi sur place dans le style « Louis XIII ».<br>
À la mort du roi, en 1643, le village de Versailles avait encore peu changé.</p><br><br>
	
