	<h3>LES ORIGINES</h3><br><br><br>
		<p>Le site de Versailles n'était probablement pas habité à l'époque préhistorique dans la mesure où on n'y a retrouvé aucun vestige archéologique26. Cependant comme les terrains ont été fortement bouleversés lors des travaux de construction du château et de l'aménagement du parc, certaines traces ont pu être détruites. Dans les environs immédiats, des allées couvertes de l'époque néolithique, appartenant à la civilisation « Seine-et-Marne-Oise » ont été retrouvées à L'Étang-la-Ville et à Marly-le-Roi.<br><br>Au temps des Gallo-Romains, le site se trouvait sur le tracé de la voie menant de Paris à la Normandie via Villepreux et Neauphle-le-Château.<br><br>La première mention de Versailles est cité dans une charte, datée de l'an 1038, de l'abbaye de Saint-Père de Chartres dans laquelle est cité le nom d'un seigneur local, un certain Hugues de Versailles (Hugo de Versalliis). Ce serait le premier seigneur connu de Versailles.<br><br>Une deuxième allusion apparaît en 1065 dans un acte par lequel un certain Geoffroy de Gometz fonda à cette date le prieuré de Bazainville, non loin de Houdan, qu'il donna à l'abbaye de Marmoutier de Tours. Pour assurer des ressources régulières et suffisantes, il lui accorde plusieurs terres et privilèges, avec en particulier « trois prébendes à Versailles dont l'une se trouve in domino ». De ces trois prébendes canoniales, on peut émettre l'hypothèse que celle in domino relevait du seigneur de Versailles, les deux autres de l'abbaye tourangelle. Le village de Versailles serait donc né vers le milieu du xie siècle d'une double initiative seigneuriale et religieuse.<br><br>Dans le système féodal de la France médiévale, les seigneurs de Versailles étaient subordonnés directement au roi, sans suzerain intermédiaire entre eux et le roi. Ils n’étaient pas alors d’un rang très important.<br><br>À la fin du xie siècle, le premier village s’était établi auprès d’un manoir médiéval et autour de l’église Saint-Julien. La paroisse Saint-Julien de Ve